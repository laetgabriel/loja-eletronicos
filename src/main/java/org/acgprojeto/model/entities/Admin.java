package org.acgprojeto.model.entities;

import org.acgprojeto.dto.AdminDTO;

public class Admin {

    private Integer id;
    private String nome;
    private String email;
    private String senha;

    public Admin() {}

    public Admin(Integer id, String nome, String email, String senha) {
        this.id = id;
        this.nome = nome;
        this.email = email;
        this.senha = senha;
    }

    public Admin(AdminDTO adminDTO) {
        id = adminDTO.getId();
        nome = adminDTO.getNome();
        email = adminDTO.getEmail();
        senha = adminDTO.getSenha();
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    @Override
    public String toString() {
        return "Admin{" +
                "id=" + id +
                ", nome='" + nome + '\'' +
                ", email='" + email + '\'' +
                ", senha='" + senha + '\'' +
                '}';
    }
}
